package com.example.shiheng.mymusicplayer.model;

public class Music {
    int id;
    String title;
    String album;
    String artist;
    String path;
    int duration;
    int size;

    public int getId() {
        return id;
    }

    public String getTitle() {
        return title;
    }

    public String getAlbum() {
        return album;
    }

    public String getArtist() {
        return artist;
    }

    public String getPath() {
        return path;
    }

    public int getDuration() {
        return duration;
    }

    public int getSize() {
        return size;
    }
}
